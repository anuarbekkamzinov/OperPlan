    <h4>2.1 Пути возможного распространения пожара:</h4>
    <p>
      В зданиях огонь распространяется в основном по горючим материалам,
      мебели и оборудованию, находящемуся в
      помещениях, со скоростью 0,5-1,5 м/мин. Из помещений огонь
      и продукты сгорания распространяются в коридоры. Если
      лестничные клетки не отделены от коридоров, тогда продукты
      сгорания и огонь быстро распространяются на
      вышерасположенные этажи и могут отрезать пути эвакуации учащимся.
    </p>
    <p>
      Быстрому распространению огня и дыма способствуют системы вентиляции,
      воздушного отопления, а также пустоты
      конструкциях зданий. Быстрому развитию пожара способствует наличие
      легковоспламеняющихся веществ, мебели,
      различных материалов в складских помещениях и др.
    </p>

    <h4>2.2Степень угрозы жизни и здоровью людей:</h4>
    <p>
      При пожарах наибольшую опасность представляют этажи,
      где расположены классы, библиотеки, т.к. в них
      дети школьного возраста.
    </p>
    <p>
      При пожаре в ГУ Средней школы № 1 прежде всего создастся опасность учащимся. Наибольшую
      опасность представляют продукты сгорания в классах.
    </p>
    <p>
      По прибытии на пожар РТП необходимо немедленно установить
      связь с администрацией и обслуживающим персоналом
      учреждения, уточнить, какие меры приняты по эвакуации детей,
      их количество, место эвакуации, а также какой
      обслуживающий персонал можно привлечь для эвакуации детей.
    </p>
    <p>
      Для спасательных работ во всех случаях привлекают персонал учреждения,
      особенно при проведении эвакуации
      людей. В этих условиях способы и приемы спасания определяют
      с учетом сложившейся обстановки. При эвакуации дети
      выходят самостоятельно в указанном направлении или под надзором персонала и пожарных.
    </p>
    <p>
      Одновременно с тушением РТП и командиры на боевых участках
      определяют пути распространения горения, оперативно
      вводят силы и средства для защиты смежных с горящими помещений.
      Принимают меры для защиты от огня, дыма и
      проливаемой воды.
    </p>

    <h4>2.3 Места возможного обрушений строительных конструкций:</h4>
    <p>
      Стены и перегородки имеют пустоты, которые нередко соединяются с пустотами междуэтажных и
      чердачных перекрытий через не плотности и щели в местах их сочленения.
    </p>
    <p>Пожарная нагрузка в зданиях ГУ СШ№1 в помещениях 20-40 кг/м2.</p>
    <p>
      Из-за большой пожарной нагрузки в&nbsp;при возникновении пожара местом возможного обрушения
      междуэтажных и чердачных перекрытий возникает как правило над очагом интенсивного горения, так
      как происходит перенагревание из-за высокой температуры железобетонного перекрытия. А также
      этому способствуют имеющиеся пустоты в перекрытиях. . В связи с этим&nbsp;возможно обрушение
      конструкций железобетонного перекрытия&nbsp;4&nbsp;этажа над&nbsp;очагом интенсивного
      горения,&nbsp;а также под очагом горения перекрытия&nbsp;&nbsp;&nbsp;помещений.</p>
    <p>
      Самая опасная стадия пожара &ndash; это вторая, так как&nbsp;эта стадия пожара оказывает
