      достигает от 300 до 1200 град..</p>

    <h4>2.4 Возможные зоны задымления и прогнозируемая концентрация продуктов горения:</h4>
    <p>
      Объем выделившегося дыма, его плотность и токсичность зависят от свойств горящего материала и
      от условий протекания процесса горения.
    </p>
    <p>
      Под зоной задымления понимается часть
      пространства, примыкающего к зоне горения, в котором невозможно пребывание людей без защиты
      органов дыхания и в котором затрудняются боевые действия подразделений пожарной охраны из-за
      недостатка видимости.
    </p>
    <p>
      В зданиях II степеней огнестойкости, к которым в основном относятся здания ГУ &laquo;СШ
      №1&raquo;, огонь распространяется в основном по горючим материалам, мебели и оборудованию,
