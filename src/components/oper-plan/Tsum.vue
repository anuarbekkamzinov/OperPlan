    <h4>Планировка территории:</h4>
    <p>
      Средняя школа №1 находится в западной части города Рудного по адресу ул.
      Дзержинского 19, на расстоянии от СПЧ-2 г. Рудного 0,5 км.
      Территория занимаемая объектом имеет общую площадь -1 Га,
      на ней расположено одно здание - Средней школы № 1 общей площадью- 3542,5 м2,
      мастерская общей площадью – 512,6 м2 , гараж общей площадью -89,88 м2.
      Территория ограждена забором из железной арматуры.
      Рельеф участка ровный, территория частично асфальтирована.
      Функциональное назначение объекта –образовательное .Год постройки-1959 г.
    </p>

