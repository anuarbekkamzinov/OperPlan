      Возможные зоны задымления и
      прогнозируемая концентрация продуктов горения в ГУ &laquo;СШ №1&raquo; при возникновении
      пожара в отделениях находящихся на 1-ом этаже можно считать практически все помещения данного
      отделения и помещения 2-го этажа.
    </p>

    <h4>
