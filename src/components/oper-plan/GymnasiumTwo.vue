    <h3 class="text-center">
      4.10. РЕКОМЕНДАЦИИ для ответственного за технику безопасности
      при тушении пожара в Гимназии №2.
    </h3>

    <p>
      По прибытии на пожар РТП немедленно устанавливает связь с администрацией и
      обслуживающим персоналом , уточняет, какие меры приняты по эвакуации людей,
      количество людей, подлежащих эвакуации, их состояние, место эвакуации, а
      также какой обслуживающий персонал можно привлечь для эвакуации. РТП быстро
      оценивает достаточность сил для эвакуации из опасных помещений и определяет
      необходимость вызова дополнительных сил и средств на пожар.
    </p>
    <p>
      Разведку пожара организуют в нескольких направлениях. В процессе разведки
      определяют угрозу от огня и дыма и пути эвакуации, месторасположения
      эвакуируемых и их количество, способность самостоятельно передвигаться,
      последовательность спасательных работ, кратчайшие и безопасные пути
      эвакуации, место возникновения и размеры зоны горения и задымления.
    </p>
    <p>
      Все спасательные работы организуют и проводят под контролем опытных
      работников пожарной охраны. При эвакуации больных по нескольким
      направлениям на каждое из них РТП назначает ответственных лиц, а сам
      возглавляет эвакуацию на наиболее ответственном участке и одновременно
      осуществляет руководство боевыми действиями по тушению пожара.
    </p>
    <p>
      После эвакуации РТП тщательно проверяет все помещения, пути, по которым она
      проводилась, а обслуживающий персонал проверяет людей по спискам.
      Поисково-спасательные работы заканчиваются тогда, когда все люди спасены.
    </p>
    <p>
      Для быстрой и слаженной работы личного состава пожарных подразделений и
      обслуживающего персонала, администрацией заранее разрабатывается план
      эвакуации, в котором указаны действия обслуживающего персонала,
      отрабатывается план на тактических учениях совместно с персоналом и один
      его экземпляр включают как составную часть в план пожаротушения.
    </p>
    <p>
      Для тушения пожаров, как правило, используют стволы РСК-50 и РС-50,
      распыленные и компактные струи. Количество стволов для тушения пожаров
      определяют с учетом требуемой интенсивно подачи воды, не менее 0,1 л/(м2
      с).
    </p>
    <p>
      В зависимости от обстановки и количества сил и средств РТП может
      одновременно организовать работы по спасанию людей и тушению пожаров. Если
      сил и средств недостаточно для одновременного решения этих двух задач. РТП
      может использовать все силы и средства для эвакуации людей или при
      уверенности, что пожар можно быстро потушить и обеспечить безопасность
      людям, для подачи стволов и предотвращения паники среди больных.
    </p>
