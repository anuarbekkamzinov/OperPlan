      </p>
      <p>
        Из-за большой пожарной нагрузки в при возникновении пожара местом
        возможного обрушения междуэтажных перекрытий возникает как правило над
        очагом интенсивного горения, так как происходит перенагревание из-за
        высокой температуры железобетонного перекрытия. А также этому способствуют
        имеющиеся пустоты в перекрытиях. . В связи с этим возможно обрушение
        конструкций железобетонного перекрытия 3 этажа над очагом интенсивного
        горения, а также под очагом горения перекрытия помещений.
      </p>
      <p>
        Самая опасная стадия пожара – это вторая, так как эта стадия пожара
        оказывает существенного влияния на огнестойкость строительных конст­рукций,
        поскольку температуры достигает от 300 до 1200 град..
      </p>
      <p>
        <strong></strong>
      </p>
      <p>
        <strong>
          2.4 Возможные зоны задымления и прогнозируемая концентрация продуктов
          горения:
        </strong>
      </p>
      <p>
        Объем выделившего­ся дыма, его плотность и токсичность зависят от свойств
        горящего материа­ла и от условий протекания процесса горения.
      </p>
      <p>
        Под зоной задымления понимается часть пространства, примыкающего к зоне
        горения, в котором невозможно пребывание людей без защиты орга­нов дыхания
        и в котором затрудняют­ся боевые действия подразделений пожарной охраны
        из-за недостатка видимости.
      </p>
      <p>
        В зданиях II степеней огнестойкости, к которым в основном относятся здания
        ГУ «Гимназии № 2», огонь распространяется в основном по горючим материалам,
        мебели и оборудованию, находящемуся в помещениях, со скоростью 0,5-1,5
        м/мин. Из помещений огонь и продукты сгорания распространяются в коридоры.
        При этом, продукты сгорания и огонь быстро распространяется на
        вышерасположенный этаж и могут отрезать пути эвакуации. Также быстрому
        задымлению этажей зданий способствуют лестничные клетки.
      </p>
      <p>
        Быстрому распространению огня и дыма способствуют системы вентиляции, а
        также пустоты в конструкциях зданий.
      </p>
      <p>
        При пожарах в зданиях и соору­жениях опасные факторы пожара являются
        основным препятствием для успешного выполнения боевой работы личным
        составом, создают опасность для жизни и здоровья людей, оказав­шихся в зоне
        задымления.
      </p>
