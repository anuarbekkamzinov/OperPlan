      дополнительные силы для проведения разведки во всех направлениях с задачей
      отыскания пострадавших.
    </p>
    <p>
      <strong> </strong>
      При спасании людей и имущества на пожаре оперативные должностные лица
      обязаны определить порядок и способы спасания людей в зависимости от
      обстановки и состояния людей, которым необходимо оказать помощь,
      предпринять меры по защите спасаемых от опасных факторов пожара.
    </p>
    <p>
      Работы по спасанию проводятся быстро, но с соблюдением предосторожностей,
      чтобы не были причинены повреждения и травмы спасаемым людям.
    </p>
    <p>
      Во всех случаях, когда проводятся спасательные работы, должностные лица
      одновременно с развертыванием сил и средств организуют вызов скорой
      медицинской помощи, даже если в данный момент в ней нет необходимости.
    </p>
    <h3 class="text-center">Раздел № 4 «Организация тушения пожара подразделениями противопожарной
      службы»
    </h3>
    <p>
      <u><strong> </strong></u>
    </p>
    <p>
      <u><strong>4.1 </strong></u>
      <u><strong>ВАРИАНТ №-</strong></u>
      <u><strong>1.</strong></u>
    </p>
    <p>
      В помещении библиотеки горючая загрузка в основном составляет деревянные и
      полимерные материалы, которые не вступают во взаимодействие с водой при их
      тушении. Пылевые аэрогели в помещении отсутствуют, по этому воду можно
      подавать компактными и распыленными струями.
    </p>
    <p>
      Определяем площадь тушения:
    </p>
    <p>
      Sт1 = n * a * hт = 1 * 5,45* 5 = 27,25 м2 .
    </p>
    <p>
      Определяем необходимое количество стволов для локализации пожара:
    </p>
    <p>
      Q трт= Sт * jттр=27*0,06 = 1,62 л/с
    </p>
    <p>
      Nтст = Qтрт / qприб = 1,62 / 3,5 = 0,46 принимаем на тушение 1 ствола
      РСК-50.
    </p>
    <p>
      Прибывшее первое подразделение производит боевое развертывание стволов в
      библиотеку и приступает к эвакуации пострадавших всеми имеющимися силами.
      Отделение ВГСО прибывшее вторым через 1 минуту приступает к эвакуации
      пострадавших, часть сил и средств подразделения прибывшего первым
      приступает к тушению - 1 ствол «Б» звеном ГДЗС подается со стороны
      коридора. Условия локализации выполняются.
    </p>
    <p>
      Определяем необходимое количество стволов на защиту объекта:
    </p>
    <p>
      Исходя из возможной обстановки на пожаре и тактических условий проведения
      боевых действий площадь защиты можно принять равной площади пожара и
      включить половину площадей смежных помещений.
    </p>
    <p>
      Q трз= Sз*Jтрт/3=(48,1+24,05+8,83)*0,02=1,61 л/с.
    </p>
    <p>
      Nстт= Qз / qст=1,61/3,5= 0,46 исходя из возможной обстановки на пожаре и
      тактических условий проведения боевых действий принимаем 2 ст «Б»
    </p>
    <p>
      - на защиту и охлаждение смежных помещений звеном ГДЗС со стороны коридора
      – 1 ствол РСК-50 («Б»);
    </p>
    <p>
      - на защиту и охлаждения крыши над очагом возгорания звеном ГДЗС – 1 ствол
      РСК-50 («Б»).
    </p>
    <p>
      Определяем требуемый расход подачи огнетушащих средств на тушение и защиту:
    </p>
    <p>
      Qтрт-з = Q трз + Q трт = 1,61 + 1,62 = 3,23 л/с.
    </p>
    <p>
      ОПРЕДЕЛЯЕМ ФАКТИЧЕСКИЙ РАСХОД ОГНЕТУШАЩИХ СРЕДСТВ НА ТУШЕНИЕ ПОЖАРА И ДЛЯ
      ЗАЩИТЫ:
    </p>
    <p>
      Qф = Nстт * qприб + Nстз * qприб = 1 * 3,5 + 2 * 3,5 = 10,5 л/с.
    </p>
    <p>
      Следовательно, условия локализации выполняется: Qф &gt; Qтрт-з
    </p>
    <p>
      ПРОВЕРЯЕМ ОБЕСПЕЧЕННОСТЬ ОБЪЕКТА ВОДОЙ:
    </p>
    <p>
      ПГ-98 расположен на кольцевой водопроводной сети диаметром 300 мм на
      расстоянии 25 м. от места пожара, при напоре в сети Н=30 м вод.ст. (3 атм.)
      скорость движения воды будет равна -1,5 м/с (Vв = 1,5 м/с), согласно данным
      таблицы 4.2 справочника РТП:
    </p>
    <p>
      Q к. водоот. = (Vв * dс)2;
    </p>
    <p>
      dс = 300 мм.= 11,8 дюйма, ( 1 дюйм = 25,4)
    </p>
    <p>
      Q к. водоот. = ( 1,5 * 11,8)2 = 113,0 л/с.
    </p>
    <p>
      Следовательно, ПГ-98 обеспечит тушение пожара необходимым количеством воды,
      условия Q общ. ф &gt; Q к. вод.. выполняется, так как, 113,0 л/с &gt; 10,5
      л/с
    </p>
    <p>
      ОПРЕДЕЛЯЕТСЯ ТРЕБУЕМОЕ КОЛИЧЕСТВО ПОЖАРНЫХ МАШИН С УЧЕТОМ ИСПОЛЬЗОВАНИЯ
      НАСОСОВ ПО СХЕМЕ: 3 ствола РСК-50 («Б»):
    </p>
    <p>
      Nм = Qф / Qн = 10,5 /32 = 0,32 принимаем 1 АЦ-40
    </p>
    <p>
      ОПРЕДЕЛЯЕМ ПРЕДЕЛЬНОЕ РАСТОЯНИЕ ПО ПОДАЧИ ПРИБОРОВ ТУШЕНИЯ ПОЖАРА ОТ
      УСТАНОВЛЕННЫХ ПОЖАРНЫХ АВТОМОБИЛЕЙ НА ТУШЕНИЕ ПОЖАРА И ДЛЯ ЗАЩИТЫ:
    </p>
    <p>
      LПР=(Нн–(Нр+Zм+Zст)/SQ2)*20 = [90–(50+0+3,8)/0,016*17,52]*20 = 147,7 м
    </p>
    <p>
      Так как, расстояния от ближайшего ПГ 98 (К-300) до очага пожара составляет
      примерно – 25 м., следовательно, что предельное расстояние по подачи
      стволов от ПА установленного на ПГ соответствует.
    </p>
