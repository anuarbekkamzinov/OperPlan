        <strong></strong>
      </p>
      <p>
        <strong>2.2 Степень угрозы жизни и здоровью людей:</strong>
      </p>
      <p>
        <strong></strong>
      </p>
      <p>
        При пожарах наибольшую опасность представляют этажи, где расположены
        классы, библиотеки, т.к. в них дети школьного возраста.
      </p>
      <p>
        При пожаре в ГУ «Гимназию № 2» прежде всего создастся опасность учащимся.
        Наибольшую опасность представляют продукты сгорания в классах.
      </p>
      <p>
        По прибытии на пожар РТП необходимо немедленно установить связь с
        администрацией и обслуживающим персоналом учреждения, уточнить, какие меры
        приняты по эвакуации детей, их количество, место эвакуации, а также какой
        обслуживающий персонал можно привлечь для эвакуации детей.
      </p>
      <p>
        Для спасательных работ во всех случаях привлекают персонал учреждения,
        особенно при проведении эвакуации людей. В этих условиях способы и приемы
        спасания определяют с учетом сложившейся обстановки. При эвакуации дети
        выходят самостоятельно в указанном направлении или под надзором персонала и
        пожарных.
      </p>
      <p>
        Одновременно с тушением РТП и командиры на боевых участках определяют пути
        распространения горения, оперативно вводят силы и средства для защиты
        смежных с горящими помещений. Принимают меры для защиты от огня, дыма и
        проливаемой воды.
      </p>
      <p>
        <strong></strong>
      </p>
      <p>
        <strong>2.3 Места возможного обрушений строительных конструкций:</strong>
