      окружающими ограждающими конструкциями и горючими материалами. Границы зоны
      проходят там, где тепловое воздействие приводит к заметному изменению
      состояния материалов, конструкций и создает невозможные условия для
      пребывания людей без тепловой защиты.
    </p>
    <p>
      Возможные зоны теплового воздействия и его параметры зависят от места
      возникновения пожара. Наибольшее тепловое воздействие будет наблюдаться
      непосредственно у очага пожара и в помещениях верхнего над очагом этажа, с
      заметным снижением его интенсивности по мере отдаления.
