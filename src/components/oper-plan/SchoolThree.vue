      лестничные клетки не отделены от коридоров, тогда продукты сгорания и огонь
      быстро распространяются на вышерасположенные этажи и могут отрезать пути
      эвакуации учащимся.
    </p>
    <p>
      Быстрому распространению огня и дыма способствуют системы вентиляции,
      воздушного отопления, а также пустоты в конструкциях зданий. Быстрому
      развитию пожара способствует наличие легковоспламеняющихся веществ, мебели,
      различных материалов в складских помещениях и др.
    </p>
    <p>
      <strong>2.2 </strong>
      <strong>Степень угрозы жизни и здоровью людей:</strong>
    </p>
    <p>
      При пожарах наибольшую опасность представляют этажи, где расположены
      классы, библиотеки, т.к. в них дети школьного возраста.
    </p>
    <p>
      При пожаре в ГУ «Средней школы № 3» прежде всего создастся опасность
      учащимся. Наибольшую опасность представляют продукты сгорания в классах.
    </p>
    <p>
      По прибытии на пожар РТП необходимо немедленно установить связь с
      администрацией и обслуживающим персоналом учреждения, уточнить, какие меры
      приняты по эвакуации детей, их количество, место эвакуации, а также какой
      обслуживающий персонал можно привлечь для эвакуации детей.
    </p>
    <p>
      Для спасательных работ во всех случаях привлекают персонал учреждения,
      особенно при проведении эвакуации людей. В этих условиях способы и приемы
      спасания определяют с учетом сложившейся обстановки. При эвакуации дети
      выходят самостоятельно в указанном направлении или под надзором персонала и
      пожарных.
    </p>
    <p>
      Одновременно с тушением РТП и командиры на боевых участках определяют пути
      распространения горения, оперативно вводят силы и средства для защиты
      смежных с горящими помещений. Принимают меры для защиты от огня, дыма и
      проливаемой воды.
    </p>
    <p>
      <strong>2.3 </strong>
      <strong>Места возможного обрушений строительных конструкций:</strong>
    </p>
    <p>
      Пожарная нагрузка в зданиях ГУ «СШ№3 в помещениях 20-40 кг/м2.
    </p>
    <p>
      Из-за большой пожарной нагрузки в при возникновении пожара местом
      возможного обрушения междуэтажных перекрытий возникает как правило над
      очагом интенсивного горения, так как происходит перенагревание из-за
      высокой температуры железобетонного перекрытия. А также этому способствуют
      имеющиеся пустоты в перекрытиях. . В связи с этим возможно обрушение
      конструкций железобетонного перекрытия 3 этажа над очагом интенсивного
      горения, а также под очагом горения перекрытия помещений.
    </p>
    <p>
      Самая опасная стадия пожара – это вторая, так как эта стадия пожара
      оказывает существенного влияния на огнестойкость строительных конструкций,
      поскольку температуры достигает от 300 до 1200 град..
    </p>
    <p>
      <strong>2.4 </strong>
      <strong>
        Возможные зоны задымления и прогнозируемая концентрация проду
      </strong>
      <strong>к</strong>
      <strong>тов горения:</strong>
    </p>
    <p>
      Объем выделившегося дыма, его плотность и токсичность зависят от свойств
      горящего материала и от условий протекания процесса горения.
    </p>
    <p>
      Под <strong>зоной задымления</strong> понимается часть пространства,
      примыкающего к зоне горения, в котором невозможно пребывание людей без
      защиты органов дыхания и в котором затрудняются боевые действия
      подразделений пожарной охраны из-за недостатка видимости.
    </p>
    <p>
      В зданиях II степеней огнестойкости, к которым в основном относятся здания
      ГУ «СШ №3», огонь распространяется в основном по горючим материалам, мебели
      и оборудованию, находящемуся в помещениях, со скоростью 0,5-1,5 м/мин. Из
      помещений огонь и продукты сгорания распространяются в коридоры. При этом,
      продукты сгорания и огонь быстро распространяется на вышерасположенный этаж
      и могут отрезать пути эвакуации. Также быстрому задымлению этажей зданий
      способствуют лестничные клетки.
    </p>
    <p>
      Быстрому распространению огня и дыма способствуют системы вентиляции, а
      также пустоты в конструкциях зданий.
    </p>
    <p>
      При пожарах в зданиях и сооружениях опасные факторы пожара являются
      основным препятствием для успешного выполнения боевой работы личным
      составом, создают опасность для жизни и здоровья людей, оказавшихся в зоне
      задымления.
    </p>
    <p>
      Границами зоны задымления считаются места, где плотность дыма составляет
      1-10-4÷6-10-4 кг/м3, видимость предметов 6-12 м, концентрация кислорода в
      дыме не менее 16% и токсичность газов не представляет опасности для людей,
      находящихся без средств защиты органов дыхания.
    </p>
    <p>
      Практически установить границы зон при пожаре не представляется возможным,
      так как происходит их непрерывное изменение, и можно говорить лишь об
      условном их расположении.
    </p>
    <p>
      <strong> </strong>
      Возможные зоны задымления и прогнозируемая концентрация продуктов горения в
      ГУ «СШ №3» при возникновении пожара в отделениях находящихся на 1-ом этаже
