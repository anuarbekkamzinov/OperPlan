    <h4 class="text-center">
      Планировка территории:
    </h4>
    <p>
      Средняя школа № 3 находится в западной части города Рудного по адресу ул.
      П. Корчагина 168, на расстоянии от СПЧ-2 г. Рудного 4,5 км. Въезд на
      территорию СШ осуществляется со стороны улицы П. Корчагина. Территория
      занимаемая объектом имеет общую площадь - 29715 м2 на ней расположено одно
      здание которая делит на две школы - Средняя школа № 3,9. Площадь застройки
      СШ № 3 - 2598,2 м2 , СШ № 9-1089,2 м2. Территория ограждена железобетонным
      забором. Рельеф участка ровный, территория асфальтирована 7341 м2 .
      Функциональное назначение объекта –образовательное .Год постройки-1987 г.
    </p>

    <h4 class="text-center">
      Конструктивные особенности зданий и сооружений:
    </h4>
    <p>
      Здание школы 3-х этажное, II степени огнестойкости, с геометрическими
      размерами в плане 85 х 129 м, высота здания-12 м. Наружные стены здания
      –панельные; перегородки – панельные; кровля – ж/б плиты; перекрытие - ж/б
      плиты; фундамент- ж/б блоки. Полы- дощатые, линолеум, плитка; окна-
      пластиковые пакеты ; двери- простые; внутренняя отделка- цементный раствор,
      обои ; наружная отделка- плитка. Отопление- центральное, водяное.
      Электроснабжение здания – 220 В, отключение электроэнергии в здании
      производится в помещении щитовой на 1-ом этаже, отключение электроэнергии
      на улице производится в помещении раздевалки на 1-ом этаже. Вентиляция
